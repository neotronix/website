<h3 style="margin-top:0;">Акционерное Общество «Конструкторского Бюро «Кунцево» — предприятие с 70-летним опытом работы в области радиоэлектронных систем и комплексов.</h3>

<p><em>Основными видами деятельности предприятия является проведение научно-исследовательских и опытно-конструкторских работ по радиоэлектронным и электрорадиотехническим изделиям вооружений и специальной
техники, товарам производственно-технического назначения и народного потребления.</em></p>

