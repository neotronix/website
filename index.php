перспективу, наращивание научно-технического и экономического потенциала предприятия, внедрение <b><em>инновационных методов разработки</em></b>, проектирования, моделирования, настройки и испытаний при создании опытных образцов изделий, а также разработка и внедрение систем технической поддержки эксплуатации сложных радиоэлектронных комплексов со стороны разработчика и изготовителя.
