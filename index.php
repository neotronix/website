<p>За годы существования КБ являлось участником разработки и головным конструкторским бюро по серийному производству систем ПВО С-25, С-75, С-125, С-200, С-300. КБ выполняло задачи по модернизации средств систем ПВО и их сопровождении в эксплуатации.
</p>

<p>КБ самостоятельно в качестве головного разработчика создало <a href="develop-kama.php">радиолокационную станцию траекторных измерений Кама-Н</a>, <a href="develop-prb.php">передвижную ремонтную базу</a> для войскового ремонта систем С-300 и многие другие виды продукции военного, общепромышленного и гражданского применения.</p>

<p>
В 2000-2005 году АО Конструкторское Бюро «Кунцево» как головная организация в кооперации с рядом предприятий промышленности РФ и Республики Беларусь приступило к модернизации зенитного ракетного
комплекса (ЗРК) С-125М1, разработанного ЦКБ «Алмаз» с участием КБ, поставленного ранее во многие страны ближнего и дальнего зарубежья. За разработку <a href="develop-pechora.php">модернизированного комплекса «Печора-2М»</a> представители АО «КБ «Кунцево» вместе с представителями предприятий кооперации были удостоены ФС ВТС <a href="company-chronicle.php">Национальной премии «Золотая идея»</a> по итогам 2004 года как лучшие разработчики экспортно-ориентированной продукции военного назначения.</p>

<p>Предприятие оснащено современным оборудованием для автоматизированной разработки конструкторской документации, имеет участки монтажа и сборки радиоэлектронной аппаратуры.</p>

<p>Высокий научно-исследовательский уровень исследований и разработок обеспечивается опытом высококвалифицированных специалистов. Сегодня на предприятии работает <a href="company-doct.php">3 доктора и 6 кандидатов</a> технических и физико-математических наук, а также молодые перспективные выпускники ведущих российских вузов.</p>

